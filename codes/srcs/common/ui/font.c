/* ************************************************************************** */
// 文字列を左から1文字ずつ、scale倍・指定色でバックバッファに描画する
void
	draw_text_scaled(t_window* w, t_pos* pos, char* str, int scale, int color)
{
	t_pos	origin;
	int		i;

	copy_pos(&origin, pos);
	i = 0;
	while (str[i]) {
		draw_glyph(w, &origin, str[i], scale, color);
		origin.x += FONT_W * scale;
		i++;
	}
}

/* ************************************************************************** */
// 1文字分の8バイトグリフを返す（収録範囲外は空白グリフへフォールバック）
static const unsigned char*
	glyph_of(char c)
{
	unsigned char	uc;

	uc = (unsigned char)c;
	if (uc < FONT_FIRST || uc > FONT_LAST) {
		return (g_font[0]);
	}
	return (g_font[uc - FONT_FIRST]);
}

/* ************************************************************************** */
// 1文字を origin を左上として、点灯ビットを scale×scale の矩形で塗り描画する
static void
	draw_glyph(t_window* w, t_pos* origin, char c, int scale, int color)
{
	const unsigned char*	rows;
	t_pos					p1;
	t_pos					p2;
	int						gx;
	int						gy;

	rows = glyph_of(c);
	gy = 0;
	while (gy < FONT_H) {
		gx = 0;
		while (gx < FONT_W) {
			if (rows[gy] & (1 << (7 - gx))) {
				set_pos(&p1, origin->x + gx * scale, origin->y + gy * scale);
				set_pos(&p2, p1.x + scale, p1.y + scale);
				draw_rectangle(w, &p1, &p2, color);
			}
			gx++;
		}
		gy++;
	}
}
