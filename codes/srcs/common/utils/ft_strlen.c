/* ************************************************************************** */
int
	ft_strlen(const char* str);

/* ************************************************************************** */
// NUL 終端までの文字数を返す（終端文字 '\0' は数えない）。str は非NULL前提（呼び出し側で保証）
int
	ft_strlen(const char* str)
{
	size_t	i;

	i = 0;
	while (str[i]) {
		i++;
	}
	return (i);
}
