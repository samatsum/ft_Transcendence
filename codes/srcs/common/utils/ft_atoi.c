/* ************************************************************************** */
int
	ft_atoi(const char* str);

/* ************************************************************************** */
// 文字列の先頭にある10進整数を int へ変換する。先頭の空白類(空白・タブ・改行など)を読み飛ばし、
// 任意の符号 +/- を1つだけ受け付け、続く連続した数字を積み上げる。str が NULL なら 0 を返す。
// 桁あふれ(オーバーフロー)は未対策で、入力が int に収まる妥当な値であることを前提とする
int
	ft_atoi(const char* str)
{
	int	i;
	int	is_neg;
	int	res;

	if (!str) {
		return (0);
	}
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f' || str[i] == '\r' || str[i] == ' ') {
		i++;
	}
	is_neg = 1;
	if (str[i] == '-') {
		is_neg = -1;
	}
	if (is_neg == -1 || str[i] == '+') {
		i++;
	}
	res = 0;
	while (str[i] >= '0' && str[i] <= '9') {
		res = (res * 10) + (str[i] - '0');
		i++;
	}
	return (res * is_neg);
}
