// 再入可能な擬似乱数生成器（許可外の rand_r を置き換える自作版）。状態は *seed のみで
// グローバル状態を持たないため、呼び出し側が種を持ち回れば決定的・再現可能。線形合同法(LCG。
// 乗数・加数は glibc と同じ値)で *seed を更新し、下位ビットほど周期が短い偏りを避けるため
// 16bit 右シフトした 15bit [0,32767] を返す。剰余で範囲を絞る用途(% n)を想定する
unsigned int
	ft_rand(unsigned int* seed)
{
	*seed = *seed * 1103515245 + 12345;
	return ((*seed >> 16) & 0x7FFF);
}
